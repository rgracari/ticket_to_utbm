package ticket_to_utbm.game;

/**
 * Les différentes couleurs qu'un joueur peut avoir. Cet enum est utilisé afin
 * de pouvoir différencier par l'attribut de couleur les joueurs entre eux.
 */
public enum CouleurJoueur {
    /**
     * Couleur Rouge représentant un joueur
     */
	Rouge,
	
	/**
     * Couleur Vert représentant un joueur
     */
	Vert,
	
	/**
     * Couleur Bleu représentant un joueur
     */
	Bleu,
	
	/**
     * Couleur Jaune représentant un joueur
     */
	Jaune,
	
	/**
     * Couleur Noir représentant un joueur
     */
	Noir,
}
